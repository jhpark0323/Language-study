package java_03_operator;

public class operators {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);

    }
}
